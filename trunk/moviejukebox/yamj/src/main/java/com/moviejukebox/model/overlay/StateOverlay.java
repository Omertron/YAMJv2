package com.moviejukebox.model.overlay;

import com.moviejukebox.model.Movie;

/**
 * Position Overlay
 *
 * @author Stuart
 */
public class StateOverlay extends PositionOverlay {

    private String value = Movie.UNKNOWN;
    private String filename = Movie.UNKNOWN;

    public StateOverlay(Integer left, Integer top, String align, String valign, String width, String height, String value) {
        setLeft(left);
        setTop(top);
        setAlign(align);
        setValign(valign);
        setWidth(width);
        setHeight(height);
        this.value = value;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getFilename() {
        return filename;
    }

    public void setFilename(String filename) {
        this.filename = filename;
    }
}
